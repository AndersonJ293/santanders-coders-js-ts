console.log(`Tamanho do array: ${numeros.length}`);
console.log(numeros);

numeros.pop();

console.log(`Tamanho do array (primeiro pop): ${numeros.length}`);
console.log(numeros);

numeros.pop();

console.log(`Tamanho do array (segundo pop): ${numeros.length}`);
console.log(numeros);
